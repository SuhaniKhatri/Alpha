package bitwise;

public class BitwiseOperators {
    public static void main(String[] args) {
        //Bitwise AND (&)
        System.out.println((5 & 6));
        //Bitwise OR (|)
        System.out.println((5 | 6));
        //Bitwisw XOR (^)
        System.out.println((5 ^ 6));
        //Binary One's Complement
        System.out.println((~5));  //-6
        //Binary left shift
        System.out.println((5<<2));
         //Binary right shift
         System.out.println((6>>1));

    }
}
